import Fastify from 'fastify';
import cors from '@fastify/cors';
import websocket from '@fastify/websocket';
import { env, config } from './shared/config/env.js';
import { logger } from './shared/utils/logger.js';
import { AppError } from './shared/errors/AppError.js';
import { routes } from './interfaces/http/routes/index.js';
import { websocketRoutes } from './infrastructure/websocket/server.js';

const server = Fastify({
    logger: config.isDev ? false : true, // We use pino logger manually in dev
});

import fs from 'fs';
import path from 'path';

// DEBUG LOGGING FILE
const LOG_FILE = path.join(process.cwd(), 'backend-debug.log');

function debugLog(msg: string) {
    try {
        fs.appendFileSync(LOG_FILE, `[${new Date().toISOString()}] ${msg}\n`);
    } catch (e) {
        console.error('Failed to write to log file', e);
    }
}

// Register Plugins
server.register(cors, {
    origin: (origin, cb) => {
        // Allow requests with no origin (like mobile apps or curl requests)
        if (!origin) return cb(null, true);

        // Check if origin matches allowed list
        const allowedOrigins = env.CORS_ORIGIN.split(',');

        // Logic: Exato match, Wildcard, ou Chrome Extension
        if (
            allowedOrigins.indexOf(origin) !== -1 ||
            env.CORS_ORIGIN === '*' ||
            origin.startsWith('chrome-extension://')
        ) {
            cb(null, true);
        } else {
            console.log(`ðŸš« CORS BLOCKED: ${origin}`);
            cb(new Error(`Not allowed by CORS: ${origin}`), false);
        }
    },
});

server.register(websocket);

// Register Routes
server.register(routes, { prefix: '/api' });
server.register(websocketRoutes);

// Global Error Handler
server.setErrorHandler((error, request, reply) => {
    if (error instanceof AppError) {
        return reply.status(error.statusCode).send({
            status: 'error',
            code: error.code,
            message: error.message,
        });
    }

    logger.error(error);

    return reply.status(500).send({
        status: 'error',
        code: 'INTERNAL_SERVER_ERROR',
        message: 'Internal Server Error',
    });
});

// Start Server
const start = async () => {
    if (config.isProd) {
        const missing: string[] = [];
        if (!env.SUPABASE_URL) missing.push('SUPABASE_URL');
        if (!env.SUPABASE_ANON_KEY) missing.push('SUPABASE_ANON_KEY');
        if (!env.SUPABASE_SERVICE_ROLE_KEY) missing.push('SUPABASE_SERVICE_ROLE_KEY');
        if (!env.OPENAI_API_KEY) missing.push('OPENAI_API_KEY');
        if (!env.STRIPE_SECRET_KEY) missing.push('STRIPE_SECRET_KEY');
        if (!env.STRIPE_WEBHOOK_SECRET) missing.push('STRIPE_WEBHOOK_SECRET');
        if (missing.length > 0) {
            logger.warn({ missing }, 'âš ï¸ Cloud Run: set these env vars in the service for full functionality');
        }
    }
    try {
        await server.listen({ port: env.PORT, host: '0.0.0.0' });
        logger.info(`ðŸš€ Server running on port ${env.PORT}`);
        const { startReprocessJob } = await import('./application/reprocess-call-summary.js');
        startReprocessJob();
        console.log('ðŸ”„ SERVER RESTARTED - CHECKING WATCHER');
    } catch (err) {
        logger.error(err);
        process.exit(1);
    }
};

// Global Error Handlers to prevent crash
process.on('uncaughtException', (err) => {
    logger.fatal({ err }, 'ðŸ”¥ UNCAUGHT EXCEPTION - Process staying alive (investigate!)');
    debugLog(`ðŸ”¥ UNCAUGHT EXCEPTION: ${err.message}\n${err.stack}`);
});

process.on('unhandledRejection', (reason, promise) => {
    logger.error({ reason }, 'ðŸš¨ UNHANDLED REJECTION');
    debugLog(`ðŸš¨ UNHANDLED REJECTION: ${JSON.stringify(reason)}`);
});

start();
